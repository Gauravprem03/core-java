package OperatorAndExpression;

public class BitwiseOperators {
    public static void main(String []args)
    {
        int x=10,y=6,z;
        z=x&y;
        System.out.println(z);

        int i=20;
        int j;
        j=i<<1;
        System.out.println(j);
        int k=30;
        int l;
        l=k>>1;
        System.out.println(l);

    }

}

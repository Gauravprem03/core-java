package Loops;

public class ForLoop {
    public static void main(String[] args)
    {
       int i;
        for(i=0;i<10;i++)
        {
            System.out.println(i);
        }
        System.out.println("Hello");
        for(i=10;i>0;i--)
        {
            System.out.println(i);
        }
        for(System.out.println("Hello");i<10;i++)
        {
            System.out.println(i);
        }
    }
}
